const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let newExpr = []
    for (i = 0; i < expr.length; i +=10){
        newExpr.push(expr.split('').slice(i, i+10))
    }
    
    let result = newExpr.map(a => a.join('').replace(/11/g,'-').replace(/10/g,'.').replace(/00/g, ''))
                          .map(a => MORSE_TABLE[a])
                          .map (a => a == undefined? ' ' : a)
                          .join('')
    return result
}


module.exports = {
    decode
}